# Format holder

# Ctrl & D --> copy and pasting the current line
# Ctrl & Y --> delete the current line
# Ctrl & F --> search
# Ctrl & R --> replace
# Ctrl & W --> expanding the selection outwards
# Ctrl & / --> comment/uncomment (select a whole function)
# Ctrl & - --> compress the function
# Ctrl & + --> expand the function
# Ctrl & hover --> checking related info
# Ctrl & hover & click --> checking related info in detail
# Tab --> tab
# Shift & Tab --> undo tab
# Shift & Enter --> new line from any point
# Shift & F10 --> running whatever is selected on the top right
# Ctrl & Shift & F10 --> run the current file
# ALT & left click --> selecting everything in a column



